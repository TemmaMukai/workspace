package code01_04;

public class Main {

	public static void main(String[] args) {
		double PI = 3.14;
		int pie = 5;
		System.out.println("半径" + pie + "cmのパイの面積は、");
		System.out.println(pie * pie * PI);
		System.out.println("パイの面積を倍にします");
		pie = 10;
		System.out.println("半径" + pie + "㎝のパイの面積は、");
		System.out.println(pie * pie * PI);
		
	}
}
